let bioIndex = 0;

function cycleThroughBio(e) {
    let iterator = 0;
    console.log(e.target.innerHTML);
    if (e.target.innerHTML == '&lt;') {
        iterator = -1;
    } else if (e.target.innerHTML == '&gt;') {
        iterator = 1;
    }
    console.log(iterator);
    bioIndex = (bioIndex + iterator)
    if (bioIndex >= bioImgSrcs.length) {
        bioIndex = 0;
    }
    if (bioIndex < 0) {
        bioIndex = bioImgSrcs.length - 1;
    }
    console.log(bioIndex);
    document.getElementById('about-me-img').src = bioImgSrcs[bioIndex];
    document.getElementById('about-me-text').innerHTML = bioTexts[bioIndex];
}

const pageContent = document.getElementById('page-content');

function handleNav(e) {
    const clickedTab = e.target.innerHTML;
    if (clickedTab == 'About Me') {
        pageContent.innerHTML = aboutMeContent;
        document.getElementById('<').addEventListener('click', cycleThroughBio);
        document.getElementById('>').addEventListener('click', cycleThroughBio);
    } else if (clickedTab == 'Personal Ethics') {
        pageContent.innerHTML = personalEthicsContent;
    } else if (clickedTab == 'Intentional Teacher') {
        pageContent.innerHTML = intentionalTeacherContent;
    } else if (clickedTab == 'Skilled Collaborator') {
        pageContent.innerHTML = skilledCollaboratorContent;
    } else if (clickedTab == 'Observation and Assessment') {
        pageContent.innerHTML = observationAndAssessmentContent;
    }
}

function toggleScreenSizeWarning() {
    console.log(window.innerWidth);
    if (window.innerWidth < 600) {
        document.getElementById('screen-size-warning').style.display = 'flex';
        document.getElementById('content').style.display = 'none';
    } else {
        document.getElementById('screen-size-warning').style.display = 'none';
        document.getElementById('content').style.display = 'flex';
    }
}

toggleScreenSizeWarning();
document.addEventListener('click', handleNav);
window.addEventListener('resize', toggleScreenSizeWarning);
